public class Loops {
    public static void main(String[] args) {
        int z = 0;

        // A simple while loop
        while( z < 10 ) {
            System.out.println(z);
            z += 1;
        }

        // A simple for loop
        for(int i = 10; i >= 0 ; i -= 1){
            System.out.println(i);
        }

    }
}

